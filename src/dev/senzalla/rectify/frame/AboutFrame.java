    private void resetLabel() {
        if (lblGithubLink.getForeground().equals(new Color(204, 204, 204))) {
            lblLinkedinLink.setForeground(Color.YELLOW);
            lblGithubLink.setForeground(Color.YELLOW);
            lblMailLink.setForeground(Color.YELLOW);
        }else{
            lblLinkedinLink.setForeground(Color.BLUE);
            lblGithubLink.setForeground(Color.BLUE);
            lblMailLink.setForeground(Color.BLUE);
        }
    }

    private void goToUrl(String url) {
        try {
            Desktop.getDesktop().browse(new URI(url));
        } catch (IOException | URISyntaxException e) {
            System.out.println("endereço nao encontrado");
        }
    }
